package com.example.projectfuture.controller;

import org.junit.jupiter.api.Disabled;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.AutoConfigureMockMvc;
import org.springframework.boot.test.autoconfigure.web.servlet.WebMvcTest;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.http.MediaType;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.transaction.annotation.Transactional;

import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.*;
import static org.springframework.test.web.servlet.result.MockMvcResultHandlers.print;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.content;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

@DisplayName("Data Rest - API 테스트")
@Transactional
@AutoConfigureMockMvc
@SpringBootTest
public class DataRestTest {
    private final MockMvc mvc;

    public DataRestTest(@Autowired MockMvc mvc) {
        this.mvc = mvc;
    }

    @DisplayName("[api] 게시글 리스트 조회")
    @Test
    void given_whenRequestingArticles_thenReturnsArticlesJsonResponse() throws Exception {
        // Given
        // Then && When
        mvc.perform(get("/api/articles"))
                .andExpect(status().isOk())
                .andExpect(content().contentType(MediaType.valueOf("application/hal+json")));
    }

    @DisplayName("[api] 게시글 단건 조회")
    @Test
    void given_whenRequestingArticle_thenReturnsArticleJsonResponse() throws Exception {
        // Given
        // Then && When
        mvc.perform(get("/api/articles/1"))
                .andExpect(status().isOk())
                .andExpect(content().contentType(MediaType.valueOf("application/hal+json")));
    }

    @DisplayName("[api] 게시글 -> 댓글 리스트 조회")
    @Test
    void given_whenRequestingArticlesCommentsFromArticle_thenReturnsArticlesCommentsJsonResponse() throws Exception {
        // Given
        // Then && When
        mvc.perform(get("/api/articles/1/articleComment"))
                .andExpect(status().isOk())
                .andExpect(content().contentType(MediaType.valueOf("application/hal+json")));
    }

    @DisplayName("[api] 댓글 리스트 조회")
    @Test
    void given_whenRequestingArticlesComments_thenReturnsArticlesCommentsJsonResponse() throws Exception {
        // Given
        // Then && When
        mvc.perform(get("/api/articleComments"))
                .andExpect(status().isOk())
                .andExpect(content().contentType(MediaType.valueOf("application/hal+json")));
    }

    @DisplayName("[api] 댓글 단건 조회")
    @Test
    void given_whenRequestingArticlesComment_thenReturnsArticlesCommentsJsonResponse() throws Exception {
        // Given
        // Then && When
        mvc.perform(get("/api/articleComments/1"))
                .andExpect(status().isOk())
                .andExpect(content().contentType(MediaType.valueOf("application/hal+json")));
    }

    @DisplayName("[api] 회원 관련 API 는 일체 제공하지 않는다.")
    @Test
    void givenNothing_whenRequestingUserAccounts_thenThrowsException() throws Exception {
        // Given

        // When & Then
        mvc.perform(get("/api/userAccount")).andExpect(status().isNotFound());
        mvc.perform(post("/api/userAccount")).andExpect(status().isNotFound());
        mvc.perform(put("/api/userAccount")).andExpect(status().isNotFound());
        mvc.perform(patch("/api/userAccount")).andExpect(status().isNotFound());
        mvc.perform(delete("/api/userAccount")).andExpect(status().isNotFound());
        mvc.perform(head("/api/userAccount")).andExpect(status().isNotFound());
    }
}
